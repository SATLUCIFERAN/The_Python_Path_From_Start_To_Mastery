# Our fixed tuple of Jedi abilities
jedi_abilities = ("Force push", "Force pull", "mind trick")

# Access the first ability
first_ability = jedi_abilities[0]
print(f"Your first ability is {first_ability}.")
# Output: Your first ability is Force push.

# Slice to get a sub-tuple
core_abilities = jedi_abilities[0:2]
print(f"Your core abilities are {core_abilities}.")
# Output: Your core abilities are ('Force push', 'Force pull')