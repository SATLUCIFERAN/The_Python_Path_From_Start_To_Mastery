

a = 14
b = 5

print("Addition:        ", a + b)   # Output: 19
print("Subtraction:     ", a - b)   # Output: 9
print("Multiplication:  ", a * b)   # Output: 70
print("Division:        ", a / b)   # Output: 2.8  
print("Floor Division:  ", a // b)  # Output: 2    
print("Modulus:         ", a % b)   # Output: 4    
print("Exponentiation:  ", a ** 2)  # Output: 196

# Consistency check: a == (a // b) * b + (a % b)
print("\nDivision identity holds:", a == (a // b) * b + (a % b))  # True








